package HelpingClasses;

public class MyPoint
{
    private double x;
    private double y;

    public MyPoint(double x, double y)
    {
        this.x = x;
        this.y = y;
    }

    public double Getx()
    {
        return this.x;
    }
    public double Gety()
    {
        return this.y;
    }
}
